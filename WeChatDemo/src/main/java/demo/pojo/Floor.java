package demo.pojo;

public class Floor {
    String floor;
    String direction;

    public void setDirection(String direction) {
        this.direction = direction;
    }

    public void setFloor(String floor) {
        this.floor = floor;
    }

    public String getFloor() {
        return floor;
    }

    public String getDirection() {
        return direction;
    }
}
